-- Finalized canonical reporting layer v1
-- Production-safe additive migration.
-- IMPORTANT:
-- 1) Does not touch legacy tables or existing legacy data.
-- 2) Does not modify 003_collector_canonical.sql.
-- 3) Creates only canonical_* v1 tables if they do not already exist.
-- 4) In environments where 003 already created canonical_* tables, this file
--    is intentionally non-destructive and will no-op for those tables.

CREATE TABLE IF NOT EXISTS canonical_source_platforms (
    id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    source_key VARCHAR(64) NOT NULL,
    display_name VARCHAR(128) NOT NULL,
    source_type ENUM('ads', 'analytics', 'programmatic', 'social', 'search', 'other') NOT NULL DEFAULT 'ads',
    default_fact_scope ENUM('campaign', 'delivery_entity') NOT NULL DEFAULT 'delivery_entity',
    default_timezone VARCHAR(64) DEFAULT NULL,
    default_currency VARCHAR(8) DEFAULT NULL,
    is_active TINYINT(1) NOT NULL DEFAULT 1,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    UNIQUE KEY uniq_canonical_source_platforms_source_key (source_key)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Grain: 1 row per supported source platform';

