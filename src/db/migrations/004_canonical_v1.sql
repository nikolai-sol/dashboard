CREATE TABLE IF NOT EXISTS canonical_collector_runs (
    id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    source_key VARCHAR(64) NOT NULL,
    run_type ENUM('manual', 'cron', 'backfill', 'reconcile', 'preview') NOT NULL DEFAULT 'manual',
    run_mode VARCHAR(64) DEFAULT NULL,
    job_key VARCHAR(128) DEFAULT NULL,
    correlation_id VARCHAR(128) DEFAULT NULL,
    date_from DATE DEFAULT NULL,
    date_to DATE DEFAULT NULL,
    status ENUM('running', 'success', 'partial', 'failed') NOT NULL DEFAULT 'running',
    rows_read BIGINT NOT NULL DEFAULT 0,
    rows_written BIGINT NOT NULL DEFAULT 0,
    rows_updated BIGINT NOT NULL DEFAULT 0,
    error_count BIGINT NOT NULL DEFAULT 0,
    error_summary TEXT DEFAULT NULL,
    started_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    finished_at DATETIME DEFAULT NULL,
    duration_ms BIGINT DEFAULT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    KEY idx_canonical_collector_runs_source_started (source_key, started_at),
    KEY idx_canonical_collector_runs_status (status),
    KEY idx_canonical_collector_runs_job_key (job_key),
    KEY idx_canonical_collector_runs_correlation (correlation_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Grain: 1 row per collector run';

CREATE TABLE IF NOT EXISTS canonical_collector_run_events (
    id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    run_id BIGINT UNSIGNED NOT NULL,
    level ENUM('info', 'warning', 'error') NOT NULL DEFAULT 'info',
    event_type VARCHAR(128) DEFAULT NULL,
    message TEXT NOT NULL,
    event_payload JSON DEFAULT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    KEY idx_canonical_collector_run_events_run (run_id),
    KEY idx_canonical_collector_run_events_level (level),
    CONSTRAINT fk_canonical_collector_run_events_run
        FOREIGN KEY (run_id) REFERENCES canonical_collector_runs(id)
        ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Grain: 1 row per collector run event';

